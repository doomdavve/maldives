#[derive(Debug, PartialEq)]
pub enum Token<'a> {
    Integer(i32),
    Symbol(&'a [u8]),
    String(&'a [u8]),
    True,
    False,
    If,
    Else,
    Let,
    Equal,
    Greater,
    Less,
    GreaterEqual,
    LessEqual,
    Plus,
    Minus,
    Star,
    Slash,
    Function,
    BraceLeft,
    BraceRight,
    ParenLeft,
    ParenRight,
    BracketLeft,
    BracketRight,
    SemiColon,
    Comma
}
